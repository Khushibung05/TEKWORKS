day=int(input("Enter the week day :"))
if day==1:
    print("SUNDAY")
elif day==2:
    print("MONDAY")
elif day==3:
    print("TUESDAY")
elif day==4:
    print("WEDNESSDAY")
elif day==5:
    print("THURSDAY")
elif day==6:
    print("FRIDAY")
elif day==7:
    print("SATURDAY")
else:
    print("Enter a valid day")
